package com.iwe.avenger.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBIndexHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBRangeKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "avenger")
public class AvengerEntity {

	@DynamoDBIndexHashKey(attributeName = "id", globalSecondaryIndexName="id-index")
	@DynamoDBAutoGeneratedKey
	private String id;
	
	@DynamoDBHashKey(attributeName="real_name")
	private String name;
	
	@DynamoDBRangeKey(attributeName="secret_identity")
	private String secretIdentity;

	public AvengerEntity() {
		super();
	}

	public AvengerEntity(String id, String name, String secretIdentity) {
		super();
		this.id = id;
		this.name = name;
		this.secretIdentity = secretIdentity;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSecretIdentity() {
		return secretIdentity;
	}

	public void setSecretIdentity(String secretIdentity) {
		this.secretIdentity = secretIdentity;
	}

}
